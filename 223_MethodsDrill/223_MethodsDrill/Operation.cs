using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace _223_MethodsDrill
{
    public class Operation
    {
        public int getMultiply(int input, int value)
        {
            return input * value;
        }

        public int getDivide(int input, int value)
        {
            return input / value;
        }

        public int getAdd(int input, int value)
        {
            return input + value;
        }
 
    }
